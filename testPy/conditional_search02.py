# 2. 기간내 등락봉수 : [일]0봉전 100봉이내 1봉 상승 발생
# 예시 주식 가격 데이터
prices = [100, 105, 102, 110, 112, 115, 113, 116, 120, 118, 122, 125, 123]

# 최근 n일 동안의 주식 가격에 대해 현재 시점에서 직전 봉의 가격과 비교하여 상승한 경우를 필터링하는 함수
def 필터링(prices, n):
    상승한_주식들 = []
    for i in range(len(prices) - n, len(prices) - 1):  # 최근 n일 동안의 데이터에 대해서만 반복
        if prices[i] > prices[i-1]:  # 현재 시점에서 직전 봉의 가격과 비교하여 상승한 경우
            상승한_주식들.append((i, prices[i]))
    return 상승한_주식들

# 최근 5일 동안의 주식 가격에 대해 현재 시점에서 직전 봉의 가격과 비교하여 상승한 경우를 필터링
n = 5
필터링된_주식들 = 필터링(prices, n)
print(f"최근 {n}일 동안 직전 봉과 비교하여 상승한 경우:", 필터링된_주식들)

for i in range(len(prices) - n, len(prices) - 1):
    print(i, prices[i])
